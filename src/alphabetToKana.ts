export function alphabetToKana(input: string, result = ''): string {
  for (const len of [1, 2, 3]) {
    if (dictionary[input.slice(0, len)]) {
      return alphabetToKana(
        input.slice(len),
        result + dictionary[input.slice(0, len)],
      )
    }
  }
  if (input[0] === 'n') {
    return alphabetToKana(input.slice(1), result + 'ん')
  }
  if (input[0] === input[1] && input[1] !== undefined) {
    return alphabetToKana(input.slice(1), result + 'っ')
  }
  return result
}

